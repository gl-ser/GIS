void TNutation::Fundamental(double T, double T2, double T3, double* Eps0, double* L, double* Lm, double* F,
                            double* D, double* Om)
{
  try
  {
    //Средний наклон эклиптики к экватору
    *Eps0 = 0.4090928042223290 - 0.0002269655248114*T - 0.0000000028604007*T2 + 0.0000000087896720*T3;

    //Средняя аномалия Луны
    *L = 2.3555483935439407 + 8328.691422883895400*T + 0.0001517951635554*T2 + 0.0000003102807559*T3;

    //Средняя аномалия Солнца
    *Lm = 6.2400359393260230 + 628.3019560241841650*T - 0.0000027973749400*T2 - 0.0000000581776417*T3;

    //Средний аргумент широты Луны
    *F = 1.6279019339719609 + 8433.4661583184533800*T - 0.0000642717497047*T2 + 0.0000000533295049*T3;

    //Разность средних долгот Луны и Солнца
    *D = 5.1984695135799228 + 7771.3771461706410300*T - 0.0000334085107653*T2 + 0.0000000921145994*T3;

    //Средняя долгота восходящего узла орбиты Луны на эклиптике
    *Om = 2.182438624 - 33.757045936*T + 3.61429e-5*T2 + 3.88e-8*T3;
  }
  catch(...)
  {
    *Eps0 = 0.0;
    *L = 0.0;
    *Lm = 0.0;
    *F = 0.0;
    *D = 0.0;
    *Om = 0.0;
  }
}


double TNutation::StarTimeTrue(double Time)
{
double Si, dn2000, Data, T, T2, T3, omt, psi, llt, ft, dt, s, tt;
  try
  {
    dn2000 = 36526.5;  //Число суток от начала отсчета до 01.01.2000 г. + 0.5 суток

    Data = TCartography::Trunc(Time);
    tt = Time - Data;

    if (tt < 0.125)
      Data = Data-1.0;

    T = (Data-dn2000)/36525.0;  //Юлианский период от 12:00 01.01.2000 г.
    T2 = T*T;
    T3 = T*T*T;
    llt = 6.24003594e0+628.30195602e0*T-2.7974e-6*T2-5.82e-8*T3;
    ft = 1.62790193e0+8433.46615831e0*T-6.42717e-5*T2+5.33e-8*T3;
    dt = 5.19846951e0+7771.37714617e0*T-3.34085e-5*T2+9.21e-8*T3;
    omt = 2.182438624e0-33.757045936e0*T+3.61429e-5*T2+3.88e-8*T3;

    psi = -17.1996e0*sin(omt) +0.2062e0*sin(2*omt)-1.3187e0*sin(2*(ft-dt+omt))
          + 0.1426e0*sin(llt)-0.2274*sin(2*(ft+omt));

    if (tt < 0.125)
      tt = tt+0.875;
    else
      tt = tt-0.125;

    //Звездное время
    s = (24110.54841+8640184.812866*T+0.093104*T2-6.2e-6*T3+0.061165*psi)*M_PI*2.0/86400.0e0
        + M_PI*2.0*tt*1.002737811906E0;

    //Si = Frac(s/(M_PI*2.0))*M_PI*2.0;
    Si = ((s/(M_PI*2.0)) - TCartography::Trunc(s/(M_PI*2.0)))*M_PI*2.0;
  }
  catch(...)
  {
    Si = 0.0;
  }
  return Si;
}


TMatrix TNutation::OtnToAbsStarTimeMatrix(double StarTime)
{
TMatrix M;
  try
  {
    M.FirstString.x =  cos(StarTime);  M.FirstString.y =  -sin(StarTime); M.FirstString.z =  0.0;
    M.SecondString.x = sin(StarTime);  M.SecondString.y = cos(StarTime);  M.SecondString.z = 0.0;
    M.ThirdString.x =  0.0;            M.ThirdString.y =  0.0;            M.ThirdString.z =  1.0;
  }
  catch(...)
  {
    M.Empty();
  }
  return M;
}


void TNutation::Julian2000(double Time, double* T, double* T2, double* T3)
{
double dn2000, D;
  try
  {
    dn2000 = 36526.5;  //Число суток от начала отсчета до 01.01.2000 г. + 0.5 суток

    D = Time - 0.125;
    *T = (D - dn2000) / 36525.0;  //Юлианский период от 12:00 01.01.2000 г.
    *T2 = (*T) * (*T);
    *T3 = (*T2) * (*T);
  }
  catch(...)
  {
    *T = 0.0;
    *T2 = 0.0;
    *T3 = 0.0;
  }
}


void TNutation::NutationDolgotaNaklon(double T, double T2, double T3, double* Npsi, double* Neps, double* Eps0)
{
double L, Lm, F, D, Om;
std::vector<double> Arg;
unsigned int i;
  try
  {
    //Вычислить фундаментальные аргументы
    Fundamental(T, T2, T3, Eps0, &L, &Lm, &F, &D, &Om);

    Arg.clear();
    Arg.resize(sizeof(CONST_NUTATION)/sizeof(CONST_NUTATION[0]));

    for (i=1; i<=sizeof(CONST_NUTATION)/sizeof(CONST_NUTATION[0]); i++)
      Arg[i-1] = CONST_NUTATION[i-1][1-1]*L + CONST_NUTATION[i-1][2-1]*Lm + CONST_NUTATION[i-1][3-1]*F +
                 CONST_NUTATION[i-1][4-1]*D + CONST_NUTATION[i-1][5-1]*Om;

    *Npsi = 0.0;
    *Neps = 0.0;

    for (i=1; i<=sizeof(CONST_NUTATION)/sizeof(CONST_NUTATION[0]); i++)
    {
      //В этом цикле одновременно константы переводятся из десятых долей угловых милисекунд в радианы
      *Npsi = *Npsi + (CONST_NUTATION[i-1][6-1]*M_PI*10e-7/648.0 + CONST_NUTATION[i-1][7-1]*M_PI*10e-7/648.0*T)*sin(Arg[i-1]) +
              CONST_NUTATION[i-1][10-1]*M_PI*10e-7/648.0*cos(Arg[i-1]);

      *Neps = *Neps + (CONST_NUTATION[i-1][8-1]*M_PI*10e-7/648.0 + CONST_NUTATION[i-1][9-1]*M_PI*10e-7/648.0*T)*cos(Arg[i-1]) +
              CONST_NUTATION[i-1][11-1]*M_PI*10e-7/648.0*sin(Arg[i-1]);
    }
  }
  catch(...)
  {
    *Npsi = 0.0;
    *Neps = 0.0;
    *Eps0 = 0.0;
  }
  Arg.clear();
}


void TNutation::NutationDolgotaNaklonSimple(double T, double T2, double T3, double* Npsi, double* Neps, double* Eps0)
{
double L, Lm, F, D, Om;
  try
  {
    //Вычислить фундаментальные аргументы
    Fundamental(T, T2, T3, Eps0, &L, &Lm, &F, &D, &Om);

    *Npsi = -0.83386e-4*sin(Om) + 1.9994e-6*cos(Om)*sin(Om) - 0.63932e-5*sin(2.0*F - 2.0*D + 2.0*Om) + 0.6913e-6*sin(Lm) -
            0.11024e-5*sin(2.0*F + 2.0*Om);

    *Neps = 0.44615e-4*cos(Om) + 0.27809e-5*cos(2.0*F - 2.0*D + 2.0*Om) + 0.474e-6*cos(2.0*F + 2.0*Om);
  }
  catch(...)
  {
    *Npsi = 0.0;
    *Neps = 0.0;
    *Eps0 = 0.0;
  }
}


TMatrix TNutation::NutationMatrix(double Npsi, double Neps, double Eps0)
{
TMatrix M;
  try
  {
    M.FirstString.x = cos(Npsi);
    M.FirstString.y = -sin(Npsi)*cos(Eps0);
    M.FirstString.z = -sin(Npsi)*sin(Eps0);
    M.SecondString.x = sin(Npsi)*cos(Eps0+Neps);
    M.SecondString.y = cos(Npsi)*cos(Eps0)*cos(Eps0+Neps) + sin(Eps0)*sin(Eps0+Neps);
    M.SecondString.z = cos(Npsi)*sin(Eps0)*cos(Eps0+Neps) - sin(Eps0+Neps)*cos(Eps0);
    M.ThirdString.x = sin(Npsi)*sin(Eps0+Neps);
    M.ThirdString.y = cos(Npsi)*sin(Eps0+Neps)*cos(Eps0) - cos(Eps0+Neps)*sin(Eps0);
    M.ThirdString.z = cos(Npsi)*sin(Eps0+Neps)*sin(Eps0) + cos(Eps0+Neps)*cos(Eps0);
  }
  catch(...)
  {
    M.Empty();
  }
  return M;
}


TMatrix TNutation::PrecessionMatrix(double T, double T2, double T3)
{
TMatrix M;
double Cetta, Z, Tetta;
  try
  {
    Cetta = 0.111808609e-1*T + 0.146356e-5*T2 + 0.872e-7*T3;
    Z = 0.111808609e-1*T + 0.53072e-5*T2 + 0.883e-7*T3;
    Tetta = 0.97171735e-2*T - 0.20685e-5*T2 - 0.2028e-6*T3;

    M.FirstString.x = cos(Cetta)*cos(Z)*cos(Tetta) - sin(Cetta)*sin(Z);
    M.FirstString.y = -sin(Cetta)*cos(Z)*cos(Tetta) - cos(Cetta)*sin(Z);
    M.FirstString.z = -cos(Z)*sin(Tetta);
    M.SecondString.x = cos(Cetta)*sin(Z)*cos(Tetta) + sin(Cetta)*cos(Z);
    M.SecondString.y = -sin(Cetta)*sin(Z)*cos(Tetta) + cos(Cetta)* cos(Z);
    M.SecondString.z = -sin(Z)*sin(Tetta);
    M.ThirdString.x = cos(Cetta)*sin(Tetta);
    M.ThirdString.y = -sin(Cetta)*sin(Tetta);
    M.ThirdString.z = cos(Tetta);
  }
  catch(...)
  {
    M.Empty();
  }
  return M;
}
